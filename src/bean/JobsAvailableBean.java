package bean;

public class JobsAvailableBean {
	String jobid;
	String jobTitle;
	int pin;
	String location;
	int jobsAvailble;
	int hours;
	int level;
	public String getJobid() {
		return jobid;
	}
	public void setJobid(String jobid) {
		this.jobid = jobid;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getJobsAvailble() {
		return jobsAvailble;
	}
	public void setJobsAvailble(int jobsAvailble) {
		this.jobsAvailble = jobsAvailble;
	}
	public int getHours() {
		return hours;
	}
	public void setHours(int hours) {
		this.hours = hours;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
}
